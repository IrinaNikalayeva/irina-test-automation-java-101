package module3.task1;


public class Operations extends Calc {


    public static double Summ(double a, double b) {

        return a + b;
    }

    public static double Substr(double a, double b) {

        return a - b;
    }

    public static double Multipl(double a, double b) {
        return a * b;
    }

    public static double Divide(double a, double b) {
        return a / b;
    }
}
